function getSiteMedia(siteId) {
  return GALLERY_MEDIA.filter((m) => m.siteId === siteId);
}

function getSiteStats(siteId) {
  const media = getSiteMedia(siteId);
  return {
    photos: media.filter((m) => m.type === "photo").length,
    videos: media.filter((m) => m.type === "video").length,
    links: media.filter((m) => m.type === "link").length,
    recordings: media.filter((m) => m.type === "audio").length,
    total: media.length,
  };
}

function getSitesByCategory(category) {
  return GALLERY_SITES.filter((s) => s.category === category);
}

function getCategoryStats(category) {
  const sites = getSitesByCategory(category);
  const media = GALLERY_MEDIA.filter((m) => m.category === category);
  return {
    sites: sites.length,
    photos: media.filter((m) => m.type === "photo").length,
    videos: media.filter((m) => m.type === "video").length,
  };
}

function getFestivalForSite(siteId) {
  if (siteId === "sambuokan") return GALLERY_FESTIVALS.sambuokan;
  if (siteId === "pujada-festival") return GALLERY_FESTIVALS.pujada;
  return null;
}

function getSiteById(siteId) {
  return GALLERY_SITES.find((s) => s.id === siteId) || null;
}
